const toggleIndoButton = document.getElementById("toggleIndoButton");
const toggleJawaButton = document.getElementById("toggleJawaButton");
const toggleEnglishButton = document.getElementById("toggleEnglishButton");
let currentLang = "id";

toggleIndoButton.addEventListener("click", function () {
  if (currentLang !== "id") {
    currentLang = "id";
    updateToggleButton(currentLang);
    translateText(currentLang);
  }
});

toggleJawaButton.addEventListener("click", function () {
  currentLang = "jw";
  updateToggleButton(currentLang);
  translateText(currentLang);
});

toggleEnglishButton.addEventListener("click", function () {
  currentLang = "en";
  updateToggleButton(currentLang);
  translateText(currentLang);
});

function updateToggleButton(lang) {
  // Menghapus perubahan teks pada tombol bahasa
  // toggleIndoButton.textContent = lang.toUpperCase();

  // Menonaktifkan tombol bahasa yang sedang aktif
  toggleIndoButton.disabled = lang === "id";
  toggleJawaButton.disabled = lang === "jw";
  toggleEnglishButton.disabled = lang === "en";
}

function translateText(lang) {
  const elements = document.querySelectorAll("[data-translate]");
  elements.forEach((element) => {
    const key = element.getAttribute("data-translate");
    element.textContent = translations[key][lang];
  });
}
